#include <stdio.h>

int main()
{
    int a = 10, b = 5;

    printf("\nsum of %d and %d is = %d", a, b, a + b);
    printf("\nsubstraction of %d and %d is = %d", a, b, a - b);
    printf("\nmultiplication of %d and %d is = %d", a, b, a * b);
    printf("\ndivision of %d and %d is = %d", a, b, a / b);

    return 0;
}